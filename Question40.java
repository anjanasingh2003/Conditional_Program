/*Write a Java program to check whether the given integer is a multiple of 5*/


class Question40
{
	public static void main(String[] args)
	{
	
		int num =67;    
		if(num%5==0)
		{
			System.out.println(num+" is a Multiple of 5");
		}
		else
		{
			System.out.println(num+" is not a Multiple of 5");
		}
	}
}